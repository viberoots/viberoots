async function exists(file: string): Promise<boolean> {
  try {
    await fsp.lstat(file);
    return true;
  } catch {
    return false;
  }
}

async function writeGeneratedFile(
  workspaceRoot: string,
  rel: string,
  content: string,
): Promise<void> {
  const file = path.join(workspaceRoot, rel);
  await fsp.mkdir(path.dirname(file), { recursive: true });
  if (await exists(file)) {
    const current = await fsp.readFile(file, "utf8");
    if (current === content) return;
    if (current.includes(generatedMarker)) {
      await writeIfChanged(file, content);
      return;
    }
    const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
    await fsp.mkdir(backupDir, { recursive: true });
    const backupName = rel.replaceAll("/", "__");
    const backupFile = path.join(backupDir, `${backupName}.${Date.now()}.${process.pid}.bak`);
    await fsp.copyFile(file, backupFile);
    console.error(
      `repair: ${rel} exists and is not generated by viberoots; backed up to ${backupFile}`,
    );
  }
  await writeIfChanged(file, content);
}

async function writeIfMissing(workspaceRoot: string, rel: string, content: string): Promise<void> {
  const file = path.join(workspaceRoot, rel);
  if (await exists(file)) {
    console.error(`preserve: ${rel} exists`);
    return;
  }
  await fsp.mkdir(path.dirname(file), { recursive: true });
  await fsp.writeFile(file, content, "utf8");
}

async function ensureGitignoreEntries(workspaceRoot: string): Promise<void> {
  const file = path.join(workspaceRoot, ".gitignore");
  let current = "";
  try {
    current = await fsp.readFile(file, "utf8");
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
  const existingEntries = new Set(
    current
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean),
  );
  const missing = gitignoreEntries.filter((entry) => !existingEntries.has(entry));
  if (!missing.length) return;
  const prefix = current && !current.endsWith("\n") ? "\n" : "";
  const separator = current.trim() ? "\n" : "";
  const block = `${gitignoreMarker}\n${missing.join("\n")}\n`;
  await fsp.writeFile(file, `${current}${prefix}${separator}${block}`, "utf8");
}

async function writeBuckroot(workspaceRoot: string): Promise<void> {
  const rel = ".buckroot";
  const file = path.join(workspaceRoot, rel);
  if ((await exists(file)) && (await fsp.readFile(file, "utf8")) === ".\n") return;
  if (await exists(file)) {
    const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
    await fsp.mkdir(backupDir, { recursive: true });
    const backupFile = path.join(backupDir, `${rel}.${Date.now()}.${process.pid}.bak`);
    await fsp.copyFile(file, backupFile);
    console.error(`repair: ${rel} exists and is not canonical; backed up to ${backupFile}`);
  }
  await fsp.writeFile(file, ".\n", "utf8");
}

function relativeLinkTarget(fromDir: string, target: string): string {
  const rel = path.relative(fromDir, target) || ".";
  return rel.startsWith("..") ? target : `./${rel}`;
}

async function repairCurrentSymlinkForBootstrap(
  workspaceRoot: string,
  sourcePath: string | undefined,
): Promise<void> {
  if (!sourcePath) return;
  const currentPath = path.join(workspaceRoot, ".viberoots", "current");
  const target = relativeLinkTarget(
    path.dirname(currentPath),
    path.resolve(workspaceRoot, sourcePath),
  );
  try {
    const stat = await fsp.lstat(currentPath);
    if (stat.isSymbolicLink()) {
      if ((await fsp.readlink(currentPath)) === target) return;
      await fsp.unlink(currentPath);
      await fsp.symlink(target, currentPath);
      console.error(`repair: .viberoots/current now points to ${target}`);
      return;
    }
    const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
    await fsp.mkdir(backupDir, { recursive: true });
    const backupFile = path.join(backupDir, `.viberoots__current.${Date.now()}.${process.pid}.bak`);
    await fsp.rename(currentPath, backupFile);
    console.error(
      `repair: .viberoots/current existed and was not a symlink; moved to ${backupFile}`,
    );
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
}

