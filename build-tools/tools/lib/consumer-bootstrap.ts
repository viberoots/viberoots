async function commandExists(command: string): Promise<boolean> {
  try {
    await execFileAsync(command, ["--version"]);
    return true;
  } catch {
    return false;
  }
}

async function nixDirenvReady(): Promise<boolean> {
  if (!(await commandExists("direnv"))) return false;
  for (const candidate of [
    path.join(process.env.HOME || "", ".nix-profile", "share", "nix-direnv", "direnvrc"),
    "/nix/var/nix/profiles/default/share/nix-direnv/direnvrc",
  ]) {
    if (candidate && (await exists(candidate))) return true;
  }
  return false;
}

async function runMacosDirenvBootstrap(sourcePath: string): Promise<void> {
  if (process.platform !== "darwin") {
    console.error("direnv/nix-direnv are not ready; install them before running direnv allow.");
    return;
  }
  const helper = path.join(sourcePath, "build-tools", "tools", "bootstrap", "macos-direnv.sh");
  await execFileAsync(helper, [], { maxBuffer: 1024 * 1024 * 16 });
  process.env.PATH = [
    path.join(process.env.HOME || "", ".nix-profile", "bin"),
    "/nix/var/nix/profiles/default/bin",
    process.env.PATH || "",
  ]
    .filter(Boolean)
    .join(path.delimiter);
}

async function setupDirenvIfNeeded(opts: {
  mode: DirenvSetupMode;
  sourcePath: string;
}): Promise<void> {
  if (opts.mode === "never") return;
  if (opts.mode === "auto" && (await nixDirenvReady())) return;
  await runMacosDirenvBootstrap(opts.sourcePath);
}

async function shouldAllowPnpmGenerateDuringBootstrap(opts: InitConsumerOptions): Promise<boolean> {
  if (opts.allowPnpmGenerateDuringBootstrap) return true;
  if (process.env.VBR_BOOTSTRAP_PNPM_GENERATE === "1") return true;
  if (process.env.VBR_POST_CLONE === "1" || process.env.VIBEROOTS_POST_CLONE === "1") {
    return false;
  }
  if (!(await exists(path.join(opts.workspaceRoot, ".envrc")))) return true;
  if (!(await exists(path.join(opts.workspaceRoot, ".viberoots", "workspace", "flake.nix")))) {
    return true;
  }
  return false;
}

function maybePnpmGenerateEnv(allowGenerate: boolean): NodeJS.ProcessEnv {
  if (!allowGenerate) return process.env;
  return {
    ...process.env,
    NIX_PNPM_ALLOW_GENERATE: "1",
    VBR_INSTALL_REFRESH_PNPM_HASHES: "1",
  };
}

function isPostCloneBootstrap(opts?: InitConsumerOptions): boolean {
  return (
    opts?.postClone === true ||
    process.env.VBR_POST_CLONE === "1" ||
    process.env.VIBEROOTS_POST_CLONE === "1"
  );
}

async function runOptionalDirenvAllow(
  workspaceRoot: string,
  allowPnpmGenerate: boolean,
): Promise<void> {
  const ui = createCommandUi();
  try {
    await execFileAsync("direnv", ["allow", workspaceRoot], {
      cwd: workspaceRoot,
      env: maybePnpmGenerateEnv(allowPnpmGenerate),
    });
    ui.ok("direnv allowed", workspaceRoot);
  } catch {
    console.error("direnv is not installed or not on PATH, or direnv allow failed.");
    console.error(`next: cd ${workspaceRoot}`);
    console.error("next: direnv allow && direnv reload");
  }
}

async function runInstall(workspaceRoot: string, allowPnpmGenerate: boolean): Promise<void> {
  await runInherited(
    "direnv",
    ["exec", workspaceRoot, "i"],
    workspaceRoot,
    maybePnpmGenerateEnv(allowPnpmGenerate),
  );
}

async function runInherited(
  command: string,
  args: string[],
  cwd: string,
  env: NodeJS.ProcessEnv = process.env,
): Promise<void> {
  await new Promise<void>((resolve, reject) => {
    const child = spawn(command, args, { cwd, env, stdio: "inherit" });
    child.on("error", reject);
    child.on("exit", (code, signal) => {
      if (code === 0) {
        resolve();
        return;
      }
      const suffix = signal ? ` (signal ${signal})` : "";
      reject(
        new Error(
          `install command failed: ${command} ${args.map(shellish).join(" ")} exited ${code ?? "null"}${suffix}`,
        ),
      );
    });
  });
}

function shellish(value: string) {
  return /^[A-Za-z0-9_./:=@+-]+$/.test(value) ? value : JSON.stringify(value);
}

function selectedNixCommand(): { nixBin: string; nixEnv: NodeJS.ProcessEnv } {
  const nixEnv = withSanitizedInheritedNixConfig(envWithResolvedNixBin({ ...process.env }));
  return {
    nixBin: resolveToolPathSync("nix", nixEnv),
    nixEnv,
  };
}

async function runNixFlakeLock(opts: InitConsumerOptions): Promise<void> {
  const filteredViberootsInput = await prepareFilteredViberootsInput(opts.workspaceRoot, opts);
  const workspaceFlake = `path:${path.join(opts.workspaceRoot, ".viberoots", "workspace")}`;
  const { nixBin, nixEnv } = selectedNixCommand();
  if (filteredViberootsInput) {
    await execFileAsync(
      nixBin,
      [
        "flake",
        "lock",
        "--accept-flake-config",
        "--override-input",
        "viberoots",
        `path:${filteredViberootsInput}`,
        workspaceFlake,
      ],
      { cwd: opts.workspaceRoot, env: nixEnv, maxBuffer: 1024 * 1024 * 16 },
    );
  } else {
    await execFileAsync(
      nixBin,
      [
        "flake",
        "update",
        "viberoots",
        "--refresh",
        "--accept-flake-config",
        "--flake",
        workspaceFlake,
      ],
      { cwd: opts.workspaceRoot, env: nixEnv, maxBuffer: 1024 * 1024 * 16 },
    );
  }
  const hiddenLock = path.join(opts.workspaceRoot, ".viberoots", "workspace", "flake.lock");
  if (!isPostCloneBootstrap(opts) && (await exists(hiddenLock))) {
    if (usesLocalSubmoduleSource(opts)) {
      if (await writePortableRootLockForSubmodule(opts)) return;
    }
    await fsp.copyFile(hiddenLock, path.join(opts.workspaceRoot, "flake.lock"));
  }
}

async function markBootstrapScaffoldVisibleToGit(workspaceRoot: string): Promise<void> {
  try {
    await execFileAsync("git", ["rev-parse", "--is-inside-work-tree"], {
      cwd: workspaceRoot,
    });
    const existing: string[] = [];
    for (const rel of bootstrapScaffoldPaths) {
      if (await exists(path.join(workspaceRoot, rel))) existing.push(rel);
    }
    if (existing.length > 0) {
      await execFileAsync("git", ["add", "--intent-to-add", "--", ...existing], {
        cwd: workspaceRoot,
      });
    }
  } catch {}
}

export async function initConsumer(opts: InitConsumerOptions): Promise<void> {
  const ui = createCommandUi();
  assertSupportedLocalPathSource(opts);
  const sourceMode: ConsumerSourceMode =
    opts.sourceMode || (opts.sourcePath ? "submodule" : "flake");
  const allowPnpmGenerate = await shouldAllowPnpmGenerateDuringBootstrap(opts);
  await assertPostCloneTrackedInputsReady(opts, sourceMode);
  await mkdirWithMacosMetadataExclusion(opts.workspaceRoot);
  await mkdirWithMacosMetadataExclusion(path.join(opts.workspaceRoot, ".viberoots"));
  await mkdirWithMacosMetadataExclusion(path.join(opts.workspaceRoot, ".viberoots", "workspace"));
  await mkdirWithMacosMetadataExclusion(path.join(opts.workspaceRoot, ".viberoots", "buck"));
  await mkdirWithMacosMetadataExclusion(path.join(opts.workspaceRoot, "projects"));
  await writeHostPathIfUseful(opts.workspaceRoot);

  await writeBuckroot(opts.workspaceRoot);
  await writeGeneratedFile(opts.workspaceRoot, ".buckconfig", buckconfig(sourceMode));
  await writeGeneratedFile(opts.workspaceRoot, ".envrc", envrc());
  await writeGeneratedFile(
    opts.workspaceRoot,
    ".viberoots/bootstrap/direnv-stage0.sh",
    direnvStage0(),
  );
  if (!isPostCloneBootstrap(opts) || !(await exists(path.join(opts.workspaceRoot, "flake.nix")))) {
    await writeGeneratedFile(opts.workspaceRoot, "flake.nix", rootFlakeNix(opts));
  }
  await writeGeneratedFile(
    opts.workspaceRoot,
    ".viberoots/workspace/flake.nix",
    flakeNix(opts, workspaceViberootsUrl(opts)),
  );
  await writeIfMissing(
    opts.workspaceRoot,
    ".viberoots/workspace/nixpkgs-source-registry-extension.nix",
    emptyRegistryExtension(),
  );
  await writeGeneratedFile(opts.workspaceRoot, ".viberoots/workspace/TARGETS", workspaceTargets());
  await writeIfMissing(opts.workspaceRoot, "README.md", consumerReadme(sourceMode));
  await writeIfMissing(
    opts.workspaceRoot,
    "projects/README.md",
    `# Projects

Project and application source belongs here.
`,
  );
  await writeIfMissing(opts.workspaceRoot, "projects/AGENTS.md", projectsAgentNotes(sourceMode));
  await writeIfMissing(opts.workspaceRoot, "projects/config/README.md", projectConfigReadme());
  await ensureGitignoreEntries(opts.workspaceRoot);
  await initSprinkleRefConfigs({
    dir: path.join(opts.workspaceRoot, "projects", "config"),
    workspaceRoot: opts.workspaceRoot,
  });
  await initLocalSprinkleRefValues(opts.workspaceRoot);

  await repairCurrentSymlinkForBootstrap(opts.workspaceRoot, opts.sourcePath);
  if (opts.lock !== false) {
    await runNixFlakeLock(opts);
    await repairGeneratedWorkspaceLock({ workspaceRoot: opts.workspaceRoot });
  }
  await markBootstrapScaffoldVisibleToGit(opts.workspaceRoot);
  const activation = await activateWorkspace({
    start: opts.workspaceRoot,
    env: { ...process.env, WORKSPACE_ROOT: opts.workspaceRoot },
    sourcePath: opts.sourcePath,
  });
  if (opts.allowDirenv !== false) {
    await setupDirenvIfNeeded({
      mode: opts.setupDirenv || "auto",
      sourcePath: activation.sourcePath,
    });
  }
  if (opts.allowDirenv !== false) {
    await runOptionalDirenvAllow(opts.workspaceRoot, allowPnpmGenerate);
  }
  if (opts.runInstall) await runInstall(opts.workspaceRoot, allowPnpmGenerate);
  ui.ok("workspace initialized", opts.workspaceRoot);
}
