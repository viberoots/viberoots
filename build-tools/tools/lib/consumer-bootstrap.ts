function consumerReadme(mode: ConsumerSourceMode): string {
  const sourceLine =
    mode === "submodule"
      ? "- `viberoots/`: reusable build tooling and development shell support."
      : "- `.viberoots/current`: pinned viberoots tooling source resolved from the workspace flake.";
  const docsLine =
    mode === "submodule"
      ? "See `viberoots/README.md` and `projects/README.md`."
      : "See the pinned viberoots README through `.viberoots/current/README.md` and `projects/README.md`.";
  return `# Workspace

This workspace is split between:

${sourceLine}
- \`projects/\`: application and project source.

${docsLine}
`;
}

