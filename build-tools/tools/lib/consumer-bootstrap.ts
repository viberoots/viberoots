async function exists(file: string): Promise<boolean> {
  try {
    await fsp.lstat(file);
    return true;
  } catch {
    return false;
  }
}

async function isTracked(workspaceRoot: string, rel: string): Promise<boolean> {
  try {
    await execFileAsync("git", ["ls-files", "--error-unmatch", "--", rel], {
      cwd: workspaceRoot,
    });
    return true;
  } catch {
    return false;
  }
}

async function assertPostCloneTrackedInputsReady(
  opts: InitConsumerOptions,
  sourceMode: ConsumerSourceMode,
): Promise<void> {
  if (!isPostCloneBootstrap(opts)) return;
  const tracked: Partial<Record<GuardedConsumerTrackedPath, string>> = {};
  for (const rel of guardedConsumerTrackedPaths) {
    if (await isTracked(opts.workspaceRoot, rel)) {
      tracked[rel] = await fsp.readFile(path.join(opts.workspaceRoot, rel), "utf8").catch(() => "");
    }
  }
  const stale = staleConsumerTrackedInput({
    tracked,
    expectedBuckconfig: buckconfig(sourceMode),
    expectedEnvrc: envrc(),
  });
  if (stale) {
    throw new Error(
      `error: post-clone found stale tracked generated file ${stale}\nno tracked files were modified\n\nrepair: run viberoots update`,
    );
  }
}

async function writeGeneratedFile(
  workspaceRoot: string,
  rel: string,
  content: string,
): Promise<void> {
  const file = path.join(workspaceRoot, rel);
  await fsp.mkdir(path.dirname(file), { recursive: true });
  if (await exists(file)) {
    const current = await fsp.readFile(file, "utf8");
    if (current === content) return;
    if (current.includes(generatedMarker)) {
      await writeIfChanged(file, content);
      return;
    }
    if (withoutGeneratedMarker(current) === withoutGeneratedMarker(content)) {
      await writeIfChanged(file, content);
      return;
    }
    const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
    await fsp.mkdir(backupDir, { recursive: true });
    const backupName = rel.replaceAll("/", "__");
    const backupFile = path.join(backupDir, `${backupName}.${Date.now()}.${process.pid}.bak`);
    await fsp.copyFile(file, backupFile);
    console.error(
      `repair: ${rel} exists and is not generated by viberoots; backed up to ${backupFile}`,
    );
  }
  await writeIfChanged(file, content);
}

function withoutGeneratedMarker(content: string) {
  return content.replace(new RegExp(`^# ${escapeRegExp(generatedMarker)}\\n`), "");
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

async function writeIfMissing(workspaceRoot: string, rel: string, content: string): Promise<void> {
  const file = path.join(workspaceRoot, rel);
  if (await exists(file)) {
    console.error(`preserve: ${rel} exists`);
    return;
  }
  await fsp.mkdir(path.dirname(file), { recursive: true });
  await fsp.writeFile(file, content, "utf8");
}

function pathHasExternalAgentTool(hostPath: string, workspaceRoot: string): boolean {
  const wrapperDirs = new Set(
    [".viberoots/current/build-tools/tools/bin", "viberoots/build-tools/tools/bin"].map((rel) =>
      path.resolve(workspaceRoot, rel),
    ),
  );
  for (const dir of hostPath.split(path.delimiter)) {
    if (!dir) continue;
    const resolved = path.resolve(dir);
    if (wrapperDirs.has(resolved)) continue;
    if (["codex", "claude"].some((tool) => fs.existsSync(path.join(dir, tool)))) return true;
  }
  return false;
}

async function writeHostPathIfUseful(workspaceRoot: string): Promise<void> {
  const hostPath = filterCapturedHostPath(process.env.VBR_HOST_PATH || process.env.PATH || "");
  if (!hostPath) return;
  const file = path.join(workspaceRoot, ".viberoots", "workspace", "host-path");
  if (process.env.VBR_HOST_PATH || pathHasExternalAgentTool(hostPath, workspaceRoot)) {
    await writeIfChanged(file, `${hostPath}\n`);
  }
}

function shouldExcludeViberootsInputRel(rel: string): boolean {
  const parts = rel.split(path.sep).filter(Boolean);
  if (parts.some((part) => viberootsInputExcludes.has(part))) return true;
  const base = parts.at(-1) || "";
  return (
    base === ".metadata_never_index" ||
    base === ".source-fingerprint" ||
    base === ".node_modules.lockfile-guard" ||
    base.startsWith(".node_modules.lockfile-guard.") ||
    base.endsWith(".tmp") ||
    /^.*\.(ts|tsx|js|mjs)\.[A-Za-z0-9]{6}$/.test(base) ||
    base.startsWith("result-")
  );
}

async function copyViberootsInputTree(src: string, dst: string): Promise<void> {
  await fsp.rm(dst, { recursive: true, force: true });
  await fsp.mkdir(dst, { recursive: true });
  async function visit(dir: string): Promise<void> {
    for (const entry of await fsp.readdir(dir, { withFileTypes: true })) {
      const source = path.join(dir, entry.name);
      const rel = path.relative(src, source);
      if (shouldExcludeViberootsInputRel(rel)) continue;
      const target = path.join(dst, rel);
      if (entry.isDirectory()) {
        await fsp.mkdir(target, { recursive: true });
        await visit(source);
      } else if (entry.isSymbolicLink()) {
        await fsp.mkdir(path.dirname(target), { recursive: true });
        await fsp.symlink(await fsp.readlink(source), target);
      } else if (entry.isFile()) {
        await fsp.mkdir(path.dirname(target), { recursive: true });
        await fsp.copyFile(source, target);
      }
    }
  }
  await visit(src);
  await fsp.writeFile(path.join(dst, ".metadata_never_index"), "", "utf8");
  await fsp.writeFile(path.join(dst, ".source-fingerprint"), "", "utf8");
}

async function prepareFilteredViberootsInput(
  workspaceRoot: string,
  opts: InitConsumerOptions,
): Promise<string | undefined> {
  const sourcePath = opts.sourcePath ? path.resolve(workspaceRoot, opts.sourcePath) : "";
  const localSourcePath = path.resolve(workspaceRoot, "viberoots");
  if (!sourcePath || sourcePath !== localSourcePath) return undefined;
  if (!(await exists(path.join(sourcePath, "flake.nix")))) return undefined;
  const filtered = path.join(workspaceRoot, ".viberoots", "workspace", "viberoots-flake-input");
  await copyViberootsInputTree(sourcePath, filtered);
  return filtered;
}

async function gitOutput(args: string[], cwd: string): Promise<string> {
  try {
    const { stdout } = await execFileAsync("git", args, { cwd });
    return String(stdout || "").trim();
  } catch {
    return "";
  }
}

async function localViberootsRevision(opts: InitConsumerOptions): Promise<string> {
  const sourcePath = opts.sourcePath ? path.resolve(opts.workspaceRoot, opts.sourcePath) : "";
  if (!sourcePath) return "";
  return gitOutput(["rev-parse", "HEAD"], sourcePath);
}

async function submoduleRemoteUrl(opts: InitConsumerOptions): Promise<string> {
  return (
    (await gitOutput(
      ["config", "-f", ".gitmodules", "--get", "submodule.viberoots.url"],
      opts.workspaceRoot,
    )) ||
    (opts.sourcePath
      ? await gitOutput(
          ["remote", "get-url", "origin"],
          path.resolve(opts.workspaceRoot, opts.sourcePath),
        )
      : "") ||
    "https://github.com/viberoots/viberoots.git"
  );
}

function gitFlakeUrl(url: string, rev: string): string {
  const base = url.startsWith("git+") ? url : `git+${url}`;
  const sep = base.includes("?") ? "&" : "?";
  return `${base}${sep}rev=${rev}`;
}

function gitLockUrl(url: string): string {
  return url.startsWith("git+") ? url.slice("git+".length) : url;
}

function normalizeHiddenSubmoduleWorkspaceLockText(
  opts: InitConsumerOptions,
  text: string,
): string {
  const lock = JSON.parse(text) as {
    nodes?: Record<
      string,
      {
        locked?: {
          lastModified?: number;
          lastModifiedDate?: string;
          narHash?: string;
          path?: string;
          type?: string;
        };
        original?: { path?: string; type?: string };
        parent?: unknown;
      }
    >;
  };
  const node = lock.nodes?.viberoots;
  const filteredInput = path.join(
    opts.workspaceRoot,
    ".viberoots",
    "workspace",
    "viberoots-flake-input",
  );
  if (node?.locked?.type === "path" && node.locked.path) {
    const lockedPath = path.resolve(
      opts.workspaceRoot,
      ".viberoots",
      "workspace",
      node.locked.path,
    );
    if (path.resolve(lockedPath) === path.resolve(filteredInput)) {
      node.locked.path = "./viberoots-flake-input";
      delete node.locked.lastModified;
      delete node.locked.lastModifiedDate;
      delete node.locked.narHash;
      node.original = { path: "./viberoots-flake-input", type: "path" };
      node.parent = [];
      return `${JSON.stringify(lock, null, 2)}\n`;
    }
  }
  return text;
}

async function writePortableRootLockForSubmodule(opts: InitConsumerOptions): Promise<boolean> {
  const hiddenLock = path.join(opts.workspaceRoot, ".viberoots", "workspace", "flake.lock");
  if (!(await exists(hiddenLock))) return false;
  const hiddenLockText = await fsp.readFile(hiddenLock, "utf8");
  const rootLockPath = path.join(opts.workspaceRoot, "flake.lock");
  let existingRootLock: FlakeLock | null = null;
  try {
    existingRootLock = JSON.parse(await fsp.readFile(rootLockPath, "utf8")) as FlakeLock;
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
  const rev = await localViberootsRevision(opts);
  if (!/^[0-9a-f]{40}$/i.test(rev)) return false;
  const remoteUrl = await submoduleRemoteUrl(opts);
  const workspaceFlake = `path:${path.join(opts.workspaceRoot, ".viberoots", "workspace")}`;
  const { nixBin, nixEnv } = selectedNixCommand();
  await execFileAsync(
    nixBin,
    [
      "flake",
      "lock",
      "--accept-flake-config",
      "--override-input",
      "viberoots",
      gitFlakeUrl(remoteUrl, rev),
      workspaceFlake,
    ],
    { cwd: opts.workspaceRoot, env: nixEnv, maxBuffer: 1024 * 1024 * 16 },
  );
  try {
    const candidateLock = JSON.parse(await fsp.readFile(hiddenLock, "utf8")) as FlakeLock;
    const portableLock = existingRootLock
      ? mergeViberootsLockNode(existingRootLock, candidateLock)
      : candidateLock;
    const node = portableLock.nodes?.viberoots as
      | { original?: Record<string, unknown> }
      | undefined;
    if (!node) return false;
    node.original = {
      rev,
      type: "git",
      url: gitLockUrl(remoteUrl),
    };
    await fsp.writeFile(rootLockPath, `${JSON.stringify(portableLock, null, 2)}\n`, "utf8");
  } finally {
    await fsp.writeFile(
      hiddenLock,
      normalizeHiddenSubmoduleWorkspaceLockText(opts, hiddenLockText),
      "utf8",
    );
  }
  return true;
}

async function ensureGitignoreEntries(workspaceRoot: string): Promise<void> {
  const file = path.join(workspaceRoot, ".gitignore");
  let current = "";
  try {
    current = await fsp.readFile(file, "utf8");
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
  const missing = missingConsumerGitignoreEntries(current);
  if (!missing.length) return;
  const prefix = current && !current.endsWith("\n") ? "\n" : "";
  const separator = current.trim() ? "\n" : "";
  const block = `${gitignoreMarker}\n${missing.join("\n")}\n`;
  await fsp.writeFile(file, `${current}${prefix}${separator}${block}`, "utf8");
}

async function writeBuckroot(workspaceRoot: string): Promise<void> {
  const rel = ".buckroot";
  const file = path.join(workspaceRoot, rel);
  if ((await exists(file)) && (await fsp.readFile(file, "utf8")) === ".\n") return;
  if (await exists(file)) {
    const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
    await fsp.mkdir(backupDir, { recursive: true });
    const backupFile = path.join(backupDir, `${rel}.${Date.now()}.${process.pid}.bak`);
    await fsp.copyFile(file, backupFile);
    console.error(`repair: ${rel} exists and is not canonical; backed up to ${backupFile}`);
  }
  await fsp.writeFile(file, ".\n", "utf8");
}

function relativeLinkTarget(workspaceRoot: string, fromDir: string, target: string): string {
  const workspaceRel = path.relative(workspaceRoot, target);
  if (workspaceRel.startsWith("..") || path.isAbsolute(workspaceRel)) return target;
  const rel = path.relative(fromDir, target) || ".";
  return rel.startsWith(".") ? rel : `./${rel}`;
}

async function repairCurrentSymlinkForBootstrap(
  workspaceRoot: string,
  sourcePath: string | undefined,
): Promise<void> {
  if (!sourcePath) return;
  const currentPath = path.join(workspaceRoot, ".viberoots", "current");
  const target = relativeLinkTarget(
    workspaceRoot,
    path.dirname(currentPath),
    path.resolve(workspaceRoot, sourcePath),
  );
  let shouldReportRepair = false;
  try {
    const stat = await fsp.lstat(currentPath);
    if (stat.isSymbolicLink()) {
      if ((await fsp.readlink(currentPath)) === target) return;
      await fsp.unlink(currentPath);
      shouldReportRepair = true;
    } else {
      const backupDir = path.join(workspaceRoot, ".viberoots", "workspace", "backups");
      await fsp.mkdir(backupDir, { recursive: true });
      const backupFile = path.join(
        backupDir,
        `.viberoots__current.${Date.now()}.${process.pid}.bak`,
      );
      await fsp.rename(currentPath, backupFile);
      console.error(
        `repair: .viberoots/current existed and was not a symlink; moved to ${backupFile}`,
      );
      shouldReportRepair = true;
    }
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
  await fsp.symlink(target, currentPath);
  if (shouldReportRepair) console.error(`repair: .viberoots/current now points to ${target}`);
}

