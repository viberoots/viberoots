function usesLocalSubmoduleSource(opts: InitConsumerOptions): boolean {
  if (!opts.sourcePath || !opts.viberootsUrl.startsWith("path:")) return false;
  return (
    path.resolve(opts.workspaceRoot, opts.sourcePath) ===
    path.resolve(opts.workspaceRoot, "viberoots")
  );
}

function assertSupportedLocalPathSource(opts: InitConsumerOptions): void {
  if (!opts.sourcePath || !opts.viberootsUrl.startsWith("path:")) return;
  const sourcePath = path.resolve(opts.workspaceRoot, opts.sourcePath);
  const localSourcePath = path.resolve(opts.workspaceRoot, "viberoots");
  if (sourcePath === localSourcePath) return;
  const pathRef = opts.viberootsUrl.slice("path:".length);
  const resolvedRef = path.isAbsolute(pathRef)
    ? path.normalize(pathRef)
    : path.resolve(opts.workspaceRoot, pathRef);
  if (resolvedRef === localSourcePath) return;
  if (/^\/nix\/store\/[a-z0-9]{32}-source$/.test(resolvedRef)) return;
  throw new Error(
    `error: init-consumer refuses mutable external path input ${opts.viberootsUrl}\n` +
      `local viberoots path inputs must use ${localSourcePath}\n\n` +
      `repair: place the checkout at ${localSourcePath} or use an immutable /nix/store/*-source input`,
  );
}

function workspaceViberootsUrl(opts: InitConsumerOptions): string {
  if (usesLocalSubmoduleSource(opts)) return "path:./viberoots-flake-input";
  return opts.viberootsUrl;
}

function rootViberootsUrl(opts: InitConsumerOptions): string {
  if (usesLocalSubmoduleSource(opts)) return "path:./.viberoots/workspace/viberoots-flake-input";
  return opts.viberootsUrl;
}

