test("claude --remove-worktree does not force unrelated dirty changes", async () => {
  await fsp.mkdir(scratchRoot, { recursive: true });
  const tmp = await fsp.mkdtemp(path.join(scratchRoot, "claude-wrapper-"));
  try {
    const gitRoot = path.join(tmp, "repo");
    const worktreeRoot = path.join(gitRoot, ".claude", "worktrees", "remove-me");
    await fsp.mkdir(worktreeRoot, { recursive: true });
    await fsp.writeFile(path.join(worktreeRoot, ".git"), "gitdir: fake\n", "utf8");
    await fsp.writeFile(path.join(worktreeRoot, "REAL_DIRTY"), "", "utf8");
    const fake = await makeFakeTools(tmp, gitRoot);
    const res = await $({
      cwd: gitRoot,
      stdio: "pipe",
      reject: false,
      nothrow: true,
      env: {
        ...process.env,
        PATH: `${path.dirname(wrapper)}:${fake.bin}:/usr/bin:/bin`,
      },
    })`${wrapper} --remove-worktree remove-me`;

    assert.equal(res.exitCode, 13);
    assert.equal((await fsp.stat(worktreeRoot)).isDirectory(), true);
    const log = await fsp.readFile(fake.log, "utf8");
    assert.match(log, new RegExp(`git worktree remove ${escapeRegExp(worktreeRoot)}`));
    assert.doesNotMatch(log, /git worktree remove --force/);
    assert.doesNotMatch(log, /git branch -D worktree-remove-me/);
    assert.doesNotMatch(log, /git worktree prune/);
  } finally {
    await fsp.rm(tmp, { recursive: true, force: true });
  }
});

